    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Avbokning av Car 1
        
        //Since the format of Every Action method is the same, except for withdraw/put_back
        //Then i will put Commenting for Action buttions, in this Method, and the Next One.
        
        //Every time we try to click to Reserve/Free a time, we do a Check if a Driver is selected.
        if(!(jComboBox1.getSelectedItem().equals("Ingen Förare"))){
            box_check("Avbokning", 1);
            //the box_check method is responsible for formatting of Color, enabling/disabling buttons
            
            Object something = jComboBox1.getSelectedItem();
            //This is the Object that feed into the put_back method, where we put the values
            //back on their Indexes
            
            
            put_back(something, 1); 
            //Put_back is a "Checking" method, that checks the input based on the String value and the Box
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

