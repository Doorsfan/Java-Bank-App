    private void put_back(Object input, int box){
        //We accept the input String as a object, and check to see if it's containing value is of
        //a certain one.
        
        //Each value has a assigned spot, so that the list always ends up the same/fills out known spots
        //as to keep structure.

        if(input.equals("Raul Andersson - 5 år")){
            //Box is the method that handles the actual putting back of values and indexes of their respective spots etc.
                box(input, box, 0);          
        }
        else if(input.equals("Anna Åbäck - 10 år")){
            box(input, box, 1);
        }
        else if(input.equals("Gustaf Nyström - 1 år")){
            box(input, box, 2);
        }
        else if(input.equals("Anders Loven - 2 år")){
            box(input, box, 3);
        }
        else if(input.equals("Annie Löfdahl - 3 år")){
            box(input, box, 4);
        }
        else if(input.equals("Levvy Machiabri - 5 år")){
            box(input, box, 5);
        }
        else if(input.equals("Jalmar Lövgren - 25 år")){
            box(input, box, 6);
        }
       
    }    
